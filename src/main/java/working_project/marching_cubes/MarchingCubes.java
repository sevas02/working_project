    /**
     * Интерполирует позицию вершины на ребре на основе уровня изоповерхности.
     * @param v1 Координаты первой вершины ребра
     * @param v2 Координаты второй вершины ребра
     * @param val1 Скалярное значение в v1
     * @param val2 Скалярное значение в v2
     * @param isoLevel Уровень изоповерхности
     * @return Координаты интерполированной вершины
     */
    private static double[] interpolateVertex(double[] v1, double[] v2, double val1, double val2, double isoLevel) {
        // Вычисляем параметр интерполяции mu
        double mu = Math.abs(val1 - val2) < 1e-6 ? 0.5 : Math.max(0, Math.min(1, (isoLevel - val1) / (val2 - val1)));
        // Если значения почти равны, берём середину ребра (mu = 0.5)
        return new double[]{
                v1[0] + mu * (v2[0] - v1[0]), // Интерполяция по X
                v1[1] + mu * (v2[1] - v1[1]), // Интерполяция по Y
                v1[2] + mu * (v2[2] - v1[2])  // Интерполяция по Z
        };
    }

